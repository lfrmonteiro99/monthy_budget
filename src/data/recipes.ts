/** Get recipes filtered by protein type */
export function getRecipesByProtein(protein: string): Recipe[] {
  return RECIPES.filter((r) => r.proteinBase === protein);
}

/** Get recipes suitable for a specific meal type */
export function getRecipesByMealType(mealType: "almoco" | "jantar"): Recipe[] {
  return RECIPES.filter((r) => r.suitableFor.includes(mealType));
}
